    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        duenio.setNombre(txtNomDueño.getText());
        duenio.setCel_duenio(txtCelDueño.getText());
        control.guardarDuenio(duenio);
        
        masco.setNombre(txtNombre.getText());
        masco.setRaza(txtRaza.getText());
        masco.setColor(txtColor.getText());
        masco.setAlergico((String) cmbAlergico.getSelectedItem());
        masco.setAtEspecial((String) cmbAteEsp.getSelectedItem());
        masco.setObservaciones(txtObservaciones.getText());
        masco.setDuenio(duenio); 
        control.guardarMascota(masco);
        
        JOptionPane.showMessageDialog(this, "Se ha guardado exitosamente");
    }//GEN-LAST:event_btnGuardarActionPerformed

